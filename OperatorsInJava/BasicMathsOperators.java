public class BasicMathsOperators {
    public static void main(String[] args){

        /* Basic Operator in java */

        int  a =10, b = 3;
        double ans = 0;
        System.out.println("Addition of A +  B = "+ (a + b)); /* this  is basic  addition */
        System.out.println("Subtraction of A - B = "+ (a - b)); /* this is basic subtraction */
        System.out.println("Division of A / B = "+ (a / b)); /* in this the answer what java will return that will be  in INT not in  double or float as in java ( int / int = int ) */
        System.out.println("Division with ans in point or decimal of A / B = "+ (ans = (double) a / b)); /*  this  is division using DOUBLE data type for answer in points */
        System.out.println("Multiplication of A * B = "+ (a * b)); /* this is  the  basic multiplication in java */
        System.out.println("Modulus of A % B = "+ (a % b)); /* this is the Modulus  */

    }
}
