public class LogicalOperators {
    public static void main(String[] args){
        
        boolean j = true, k = false;

        System.out.println( j && k); /* over here both should be true to get the ans true even if one is false the ans becomes fales */
        System.out.println( j || k); /* in this case if one operator is true then the ans is true */
        System.out.println( !j); /* not operator changes the the original vvalue */
        
    }
    
}
