public class NumberChecker {
    public static void main(String[] args){
        int NumberA = 100, NumberB = 0, NumberC = -1;

        if (NumberA <  0) {
            System.out.println(NumberA + " is Negative");
        }
        else if (NumberA == 0) {
            System.out.println(NumberA + " is ZERO");
        }
        else{
            System.out.println(NumberA + " is Positive");
        }



        if (NumberB <  0) {
            System.out.println(NumberB + " is Negative");
        }
        else if (NumberB == 0) {
            System.out.println(NumberB + " is ZERO");
        }
        else{
            System.out.println(NumberB + " is Positive");
        }



        if (NumberC <  0) {
            System.out.println(NumberC + " is Negative");
        }
        else if (NumberC == 0) {
            System.out.println(NumberC + " is ZERO");
        }
        else{
            System.out.println(NumberC + " is Positive");
        }
    }
    
}
